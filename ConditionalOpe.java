class ConditionalOpe {
	public static void main(String[] args) {
		int a = 40, b = 443;
		System.out.println(a > b ? 7 : "hey");

		float c = 4.5f, d = 4.5f;
		System.out.println(c > d ? 8 : 3);

		char e = 'A', f = 'B';
		System.out.println(e == f ? 4 : 5);

		System.out.println(a < c ? true : false);
		String g = "Aakash", h = "Sky";
		System.out.println(g < h ? true : false);

	}
}